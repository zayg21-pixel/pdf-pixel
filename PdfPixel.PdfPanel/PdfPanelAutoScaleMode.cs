namespace PdfPixel.PdfPanel;

public enum PdfPanelAutoScaleMode
{
    /// <summary>
    /// No automatic scaling.
    /// </summary>
    NoAutoScale,

    /// <summary>
    /// Scale to visible pages.
    /// </summary>
    ScaleToVisible,

    /// <summary>
    /// Scale to all pages.
    /// </summary>
    ScaleToWidth
}
